						<h2>La permaculture</h2>
						<p>La permaculture est une méthode systémique et globale qui vise à concevoir des systèmes (par exemple des habitats humains et des systèmes  agricoles, mais cela peut être appliqué à n'importe quel système) en s'inspirant de l'écologie naturelle (biomimétisme ou écomimétisme) et de la tradition. Elle n'est pas une méthode figée mais un « mode d'action » qui prend en considération la biodiversité de chaque écosystème. Elle ambitionne une production agricole durable, très économe en énergie (autant en ce qui concerne le carburant que le travail manuel et mécanique) et respectueuse des êtres vivants et de leurs relations réciproques, tout en laissant à la nature « sauvage » le plus de place possible. L'inspirateur de ce modèle d'agriculture naturelle est l'agriculteur japonais Masanobu Fukuoka4 (1913-2008). Cette méthode a été théorisée dans les années 1970 par les Australiens Bill Mollison (biologiste) et David Holmgren (essayiste). Le terme permaculture signifiait initialement « agriculture permanente », mais il a été par la suite étendu pour signifier « culture de la permanence » car les aspects sociaux faisaient partie intégrante d'un système véritablement durable6. Cette dernière signification est encore aujourd'hui sujette à polémique.
						Avec ce sens étendu, la permaculture forme des individus à une éthique ainsi qu'à un ensemble de principes. L'objectif étant de permettre à ces individus de concevoir leur propre environnement, et ainsi de créer des habitats humains plus autonomes, durables et résilients, et donc une société moins dépendante des systèmes industriels de production et de distribution (identifiés par Mollison comme le fondement de la destruction systématique des écosystèmes).
						Elle utilise entre autres des notions d'écologie, de paysage, d'agriculture biologique, de biomimétisme, d'éthique, de philosophie et de pédologie. La permaculture invite à mettre ces aspects théoriques en relation avec les observations réalisées sur le terrain de façon harmonieuse.</p>
